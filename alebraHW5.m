%% Задача 7 
% Постройте на одних осях 3 конических сечения, заданных 
% уравнением в полярной системе координат: ρ(1-ɛcosϕ) – a = 0. ɛ = 0.5,
% потом 1, потом 2, при этом a = 1 во всех 3 случаях. Угол ϕ задайте от –π до π с шагом 0.1*π.


eps1 = 0.5;
eps2 = 1;
eps3 = 2;
a = 1;
phi = -pi:0.1 * pi:pi;
