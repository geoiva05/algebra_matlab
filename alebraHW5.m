p2 = a ./ (1 - eps2.*cos(phi));
p3 = a ./ (1 - eps3.*cos(phi));
figure; hold on; grid on; axis equal;
plot(pol2cart(phi, p1))
plot(pol2cart(phi, p2))
plot(pol2cart(phi, p3))
hold off;

